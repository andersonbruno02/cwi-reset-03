package br.com.cwi.reset.andersonbruno.request;

import br.com.cwi.reset.andersonbruno.domain.Genero;
import br.com.cwi.reset.andersonbruno.domain.PersonagemAtor;

import java.time.LocalDate;
import java.util.List;

public class FilmeRequest {

    private String nome;
    private LocalDate anoLancamento;
    private String capaFilme;
    private List<Genero> generos;
    private Integer idDiretor;
    private Integer idEstudio;
    private List<PersonagemRequest> personagens;
    private String resumo;

    public FilmeRequest(String nome, LocalDate anoLancamento, String capaFilme, List<Genero> generos, Integer idDiretor, Integer idEstudio, List<PersonagemRequest> personagens, String resumo) {
        this.nome = nome;
        this.anoLancamento = anoLancamento;
        this.capaFilme = capaFilme;
        this.generos = generos;
        this.idDiretor = idDiretor;
        this.idEstudio = idEstudio;
        this.personagens = personagens;
        this.resumo = resumo;
    }

    public String getNome() {
        return nome;
    }

    public LocalDate getAnoLancamento() {
        return anoLancamento;
    }

    public String getCapaFilme() {
        return capaFilme;
    }

    public List<Genero> getGeneros() {
        return generos;
    }

    public Integer getIdDiretor() {
        return idDiretor;
    }

    public Integer getIdEstudio() {
        return idEstudio;
    }

    public List<PersonagemRequest> getPersonagens() {
        return personagens;
    }

    public String getResumo() {
        return resumo;
    }
}
